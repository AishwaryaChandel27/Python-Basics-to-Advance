#Conditional Expression : if-else , ternary operator , switch-case, match-case, if-elif-else, if-else-if-else etc. This are conditional statements. 
#They are used to execute a block of code based on a condition.
#Conditional statements are used to perform different actions based on different conditions.This actions are executed when the condition is true.And can be used to execute a block of code when the condition is false.

#Example of if-else statement:

num = 10
if num > 5:
    print("Number is greater than 5")
else:
    print("Number is less than or equal to 5")

