#Madlibs Game
#A madlib is a word game where one player prompts another for a list of words to substitute for blanks in a story.


print("Madlibs Game")
print("Enter the following words to create a story:")

name = input("Enter a name: ")
place = input("Enter a place: ")
verb = input("Enter a verb: ")
adjective = input("Enter an adjective: ")
noun = input("Enter a noun: ")

print(f"Once upon a time, {name} went to the {place} to {verb}.")
print(f"While there, {name} encountered a {adjective} {noun}.")
print(f"It was a very {adjective} experience, and {name} had a great time!")