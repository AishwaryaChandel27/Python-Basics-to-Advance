#Variable = A container for a value. Behaves as the value that it contains
# A variable can be changed after its first assignment
# It store data in a computer's memory like (int, float, string, boolean)

#String Variable = A series of characters surrounded by quotation marks
first_name = "Hello World"
print(first_name)

Course = "Python"
print(Course)

#Float Variable = A numerical value with a decimal point

CGPA = 3.99
print(CGPA)

price = 10.50
print(price)

#Integer Variable = A whole number without a decimal point

age = 23
print(age)

year = 2025
print(year)

#Boolean Variable = A true or false value
is_girl = True
for_sale = False