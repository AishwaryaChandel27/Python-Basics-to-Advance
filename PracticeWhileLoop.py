#Problem 1 Simple While Loop Let's consider a simple example to illustrate the concept of a while loop: ```python i = 0 while i < 5: print(i) i += 1 ``` In this example, the loop will iterate five times, printing the values 0, 1, 2, 3, and 4.

n = int(input("Enter a number: "))
while n < 5:
  print(n)
  n = n+1